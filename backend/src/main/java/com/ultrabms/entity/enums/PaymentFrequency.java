package com.ultrabms.entity.enums;

/**
 * Payment frequency enumeration
 * Defines how often tenant makes rent payments
 */
public enum PaymentFrequency {
    /**
     * Monthly payments (every month)
     */
    MONTHLY,

    /**
     * Quarterly payments (every 3 months)
     */
    QUARTERLY,

    /**
     * Yearly payments (once per year)
     */
    YEARLY
}
