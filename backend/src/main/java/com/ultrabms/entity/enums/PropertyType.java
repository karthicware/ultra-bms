/**
 * Property type enumeration.
 * Classifies properties by their primary usage.
 */
public enum PropertyType {
    /**
     * Residential property - housing and apartments
     */
    RESIDENTIAL,

    /**
     * Commercial property - offices and retail spaces
     */
    COMMERCIAL,

    /**
     * Mixed-use property - combination of residential and commercial
     */
    MIXED_USE
}
