package com.ultrabms.entity.enums;

/**
 * Lease type enumeration
 * Defines different types of rental lease agreements
 */
public enum LeaseType {
    /**
     * Fixed term lease with specific start and end dates
     */
    FIXED_TERM,

    /**
     * Month-to-month lease with flexible monthly renewal
     */
    MONTH_TO_MONTH,

    /**
     * Yearly lease with 12-month renewable term
     */
    YEARLY
}
